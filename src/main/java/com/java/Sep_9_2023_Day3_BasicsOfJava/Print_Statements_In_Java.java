package com.java.Sep_9_2023_Day3_BasicsOfJava;

public class Print_Statements_In_Java {

	public static void main(String[] args) {
		// println() is a method
		// ln means new line
		System.out.println(1+2);
		System.out.println('c');
		System.out.println(true);
		System.out.println(12.13);
		System.out.println(1>234);
		System.out.println(1 <= 3);
		System.out.println(3 >= 2);
		System.out.println(3 == 2);
		System.out.println(5 != 4 );
		System.out.println(2 <=3  &&  4 == 4   && 3 >= 1);
		//true && true && true = true
		System.out.println(2 <=3  &&  4 != 4   && 3 >= 1);
		//true && false && true = false
		System.out.println(2 ==3  ||  4 != 4   || 3 >= 1);
		//false || false || true
		System.out.println((2>3) && (3<5)  || ((3!=4) && (5!=6)) );
		//( false && true  )   ||  (true && true)
		// false  ||  true
		// true 
		
		
		
		System.out.println("Grass is Green"); 
		System.out.println("Sky is Blue");
		System.out.print("Rose is Red");
		System.out.println("Buffalo is Black");
		System.out.println("We are learning Java");
	

	}

}
