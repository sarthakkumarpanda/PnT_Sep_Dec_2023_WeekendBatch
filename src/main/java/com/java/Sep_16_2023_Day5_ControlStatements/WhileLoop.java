package com.java.Sep_16_2023_Day5_ControlStatements;

public class WhileLoop {
	// while loop is repeated if loop
	// when the no of iterations is not finite it is advised to use While loop

	public static void main(String[] args) {
		int i = 1; //initialization
		while (i < 6) { //condition or test expression
			System.out.println("Hello World");
			i++;    //iteration statement
		}
	}
}
